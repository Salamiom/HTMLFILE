class Bitwise
{
public static void main(String args[])
{
int a =5, b = 7 , res;
  res = a&b;
 System.out.println("the result is" +res);
 
 res = a|b;
 System.out.println("the result is" +res);
 
  res = a^b;
 System.out.println("the result is" +res);
 
  res = a<<b;
 System.out.println("the result is" +res);
 
  res = a>>b;
 System.out.println("the result is" +res);
 
 }}
